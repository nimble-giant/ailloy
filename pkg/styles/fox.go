// FoxArt returns styled ASCII art based on context
func FoxArt(foxType string) string {
	var art string
	var style lipgloss.Style

	switch foxType {
	case "small":
		art = SmallFox
		style = AccentStyle
	case "sleepy":
		art = SleepyFox
		style = AccentStyle.MarginBottom(1)
	case "ailloy":
		art = AilloyFox
		style = AccentStyle.MarginBottom(1)
	case "inquisitive":
		art = InquisitiveFox
		style = AccentStyle.MarginBottom(1)
	case "celebration":
		art = CelebrationFox
		style = SuccessStyle.MarginBottom(1)
	case "thinking":
		art = ThinkingFox
		style = InfoStyle.MarginBottom(1)
	case "working":
		art = WorkingFox
		style = AccentStyle.MarginBottom(1)
	default:
		art = SmallFox
		style = AccentStyle
	}

	return style.Render(art)
}

